    private void btn_CargarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_CargarActionPerformed
        // TODO add your handling code here:
        String nombre = txtField_NombreApellido.getText();
        String edad = (String)(txtField_Edad.getText());
        String DNI = (String)(txtField_Dni.getText());
        String gmail = txtField_Gmail.getText();
        String posicion = txtField_Posicion.getText();
        
        Jugador jug = new Jugador(nombre, edad, DNI, gmail, posicion);
        jug.save();
        
        txtField_NombreApellido.setText("");
        txtField_Edad.setText("");
        txtField_Dni.setText("");
        txtField_Gmail.setText("");
        txtField_Posicion.setText("");
    }//GEN-LAST:event_btn_CargarActionPerformed

